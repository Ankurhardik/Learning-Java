public class PrettyPrinting {
    public static void main(String[] args) {
        float a=23.8482f;
        System.out.printf("%.2f", a);//by this we can terminate it by 2 decimal points
        System.out.println();
        System.out.printf("this is %s and %s","smooth","shiny");//we can place the words as per queue
// %c - Character
// %d - Decimal number (base 10)
// %e - Exponential floating-point number
// %f - Floating-point number
// %i - Integer (base 10)
// %o - Octal number (base 8)
// %s - String
// %u - Unsigned decimal (integer) number
// %x - Hexadecimal number (base 16)
// %t - Date/time
// %n - Newline
    }
}
